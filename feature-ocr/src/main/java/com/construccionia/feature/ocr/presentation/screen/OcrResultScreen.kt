/**
 * Pantalla de resultados OCR con texto extraído editable.
 */
@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun OcrResultScreen(
    onNavigateBack: () -> Unit = {},
    onNavigateToExport: (String) -> Unit = {},
    viewModel: OcrViewModel = hiltViewModel()
) {
    val editedText by viewModel.editedText.collectAsState()

    Scaffold(
        topBar = {
            TopAppBar(
                title = { Text("Texto Extraído") },
                navigationIcon = {
                    IconButton(onClick = onNavigateBack) {
                        Icon(
                            imageVector = Icons.AutoMirrored.Filled.ArrowBack,
                            contentDescription = "Volver"
                        )
                    }
                },
                colors = TopAppBarDefaults.topAppBarColors(
                    containerColor = MaterialTheme.colorScheme.primary,
                    titleContentColor = MaterialTheme.colorScheme.onPrimary,
                    navigationIconContentColor = MaterialTheme.colorScheme.onPrimary
                )
            )
        }
    ) { paddingValues ->
        Column(
            modifier = Modifier
                .fillMaxSize()
                .padding(paddingValues)
                .padding(16.dp)
                .verticalScroll(rememberScrollState())
        ) {
            Text(
                text = "Texto reconocido",
                style = MaterialTheme.typography.titleMedium
            )

            Spacer(modifier = Modifier.height(8.dp))

            Text(
                text = "Puedes editar el texto antes de exportarlo",
                style = MaterialTheme.typography.bodySmall,
                color = MaterialTheme.colorScheme.onSurfaceVariant
            )

            Spacer(modifier = Modifier.height(16.dp))

            OutlinedTextField(
                value = editedText,
                onValueChange = { viewModel.updateEditedText(it) },
                label = { Text("Texto extraído") },
                placeholder = { Text("Edita el texto reconocido aquí...") },
                supportingText = { Text("Puedes corregir errores del OCR antes de exportar") },
                modifier = Modifier
                    .fillMaxWidth()
                    .height(300.dp),
                minLines = 10,
                maxLines = 30,
                singleLine = false
            )

            Spacer(modifier = Modifier.height(16.dp))

            Row(
                modifier = Modifier.fillMaxWidth(),
                horizontalArrangement = Arrangement.spacedBy(8.dp)
            ) {
                OutlinedButton(
                    onClick = onNavigateBack,
                    modifier = Modifier.weight(1f)
                ) {
                    Icon(
                        imageVector = Icons.Default.Edit,
                        contentDescription = null,
                        modifier = Modifier.size(18.dp)
                    )
                    Spacer(modifier = Modifier.width(4.dp))
                    Text("Volver")
                }
                Button(
                    onClick = { onNavigateToExport(editedText) },
                    modifier = Modifier.weight(1f)
                ) {
                    Icon(
                        imageVector = Icons.Default.FileDownload,
                        contentDescription = null,
                        modifier = Modifier.size(18.dp)
                    )
                    Spacer(modifier = Modifier.width(4.dp))
                    Text("Exportar texto")
                }
            }
        }
    }
}
